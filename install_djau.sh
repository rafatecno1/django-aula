echo -e "   ${NEGRITA}ACCI√ìN:${RESET} Se ejecuta autom√°ticamente el script ${C_INFO}setup_djau.sh${RESET} (sin intervenci√≥n del usuario)."
echo -e "   ${C_INFO}FUNCI√ìN:${RESET} Crea la BD en PostgreSQL, configura el entorno virtual (venv), personaliza ${NEGRITA}settings_local.py${RESET}, realiza las migraciones, crea el superusuario de Django y configura la base de datos interna."
